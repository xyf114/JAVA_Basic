package com.basic.operator;

public class OperatorTest {
    public static void main(String[] args) {
        int a = 7;
        int b = 4;
        System.out.println(a/b);

        double c = 5.5;
        double d = 2.1;
        System.out.println(c/d);

        int e = 14;
        int f = 9;
        System.out.println(e%f);

        double g = 14.5;
        int h = 9;
        System.out.println(g%h);
    }
}
